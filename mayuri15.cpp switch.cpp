//Switch case
#include <iostream>
using namespace std;

int main()
{
	int n;
	cout<<"\n Enter the value of n(1 to 10):";
	cin>>n;
	
	switch(n)
	{
		case 1:cout<<"\n YOU ARE BEAUTIFUL";
				break;
		case 2:cout<<"\n NO ONE IS BETTER THAN YOU";
				break;
		case 3:cout<<"\n BE YOURSELF";
				break;
		case 4:cout<<"\n BE HAPPY";
				break;
		case 5:cout<<"\n BE POSITIVE";
				break;
		case 6:cout<<"\n HELLOW PRITTY ONE";
				break;
		case 7:cout<<"\n SMILE PLEASE";
				break;
		case 8:cout<<"n GOOD NIGHT SWEET DREAMS";
				break;
		case 9:cout<<"\n BE PROUD ON YOURSELF";
				break;
		case 10:cout<<"\n LEAVE YOUR LIFE";
				break;
		default :cout<<"\n GOOD BY and GIVE ME A SMILLE ";
	}
	return 0;
}
